// # The sea with some whirlpools (30 by 9)
// And to spice things up, add an X at the positions 25:2 and 7:9 and a 0 at the positions 6:4 and 18:7
//
// ```
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~X~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~0~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~0~~~~~~~~~~~~
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ~~~~~~X~~~~~~~~~~~~~~~~~~~~~~~
// ```

let sea = [];

for (let i = 0; i < 9; i++){
  if (i === 1){
    sea.push("~~~~~~~~~~~~~~~~~~~~~~~~X~~~~~");
  } else if (i === 3){
    sea.push("~~~~~0~~~~~~~~~~~~~~~~~~~~~~~~");
  } else if (i === 6){
    sea.push("~~~~~~~~~~~~~~~~~0~~~~~~~~~~~~");
  } else if (i === 8){
    sea.push("~~~~~~X~~~~~~~~~~~~~~~~~~~~~~~");
  } else {
    sea.push("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
  }
}

console.log(sea.join(","));
